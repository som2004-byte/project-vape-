// Helper function to get sub-categories (series) for a specific brand
export const getSubCategoriesByBrand = (brand) => {
  if (!brand) return []
  const brandProducts = PRODUCTS.filter(p => p.brand === brand)
  const uniqueSeries = [...new Set(brandProducts.map(p => p.series).filter(Boolean))]
  return uniqueSeries.sort()
}

